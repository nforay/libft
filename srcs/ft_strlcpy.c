#include "libft.h"

size_t	ft_strlcpy(char *dest, char *src, size_t size)
{
	size_t	i;

	i = 0;
	if (size == 0)
		return (ft_strlen(src));
	while (i < (size - 1) && src[i] != '\0')
	{
		if ((dest[i] = src[i]) == '\0')
			break;
		i++;
	}
	dest[i] = '\0';
	return (ft_strlen(src));
}
